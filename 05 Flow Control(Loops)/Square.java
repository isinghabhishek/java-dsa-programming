import java.util.*;

public class Square {
    public static void main(String[] args) {
        
        for(int i=0; i<=4; i++){
            System.out.println("* * * * ");
        }
        
        System.out.println("Printing Using while loop");
        
        int j=0;
        while(j<=4){
            System.out.println("* * * *");
            j++;
        }
    }
}
