

public class BitWiseOperator {
    
    public static void main(String[] args) {
// Binary AND &
        System.out.println((5 & 6));

// Binary OR |
    System.out.println((5 | 6));

// Binary XOR ^
    System.out.println((5 ^ 6));

// Binary One's Complement ~
        System.out.println((~5));
        System.out.println(~0);

// Binary Left Shift <<
    // a<<b = a*2^b
        System.out.println((5<<2));

// Binary Right Shift >>
    // a>>b = a/2^b
        System.out.println((6 >> 1));
    }
    
}
